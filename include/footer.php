    document.addEventListener('DOMContentLoaded', function() {
        const subjectBars = document.querySelectorAll('.subject-bar');

        subjectBars.forEach(subjectBar => {
            subjectBar.addEventListener('click', () => {
                const subjectContent = subjectBar.nextElementSibling;
                const icon = subjectBar.querySelector('i');

                if (subjectContent.style.display === 'none') {
                    subjectContent.style.display = 'block';
                    icon.style.transform = 'rotate(0deg)';
                } else {
                    subjectContent.style.display = 'none';
                    icon.style.transform = 'rotate(180deg)';
                }
            });
        });
    });
</script>

<script>
    
    // Lấy tham chiếu đến nút "Collapse All"
    var collapseAllButton = document.getElementById('collapseAllButton');

    // Lấy danh sách tất cả các accordion items
    var accordionItems = document.querySelectorAll('.accordion-collapse');

    // Xử lý sự kiện khi nút "Collapse All" được nhấn
    collapseAllButton.addEventListener('click', function() {
        // Duyệt qua từng accordion item và đóng chúng
        accordionItems.forEach(function(item) {
            var isExpanded = item.classList.contains('show');
            if (isExpanded) {
                item.classList.remove('show');
            }
        });
    });
</script>


